async def get_public_ip() -> str:
    async with aiohttp.ClientSession() as session:
        async with session.get("https://api.ipify.org") as response:
            return await response.text()


def sanitize_title(title):
    return (
        "".join([c for c in title if c.isalpha() or c.isdigit() or c == " "])
        .rstrip()
        .replace(" ", "_", -1)
    )


def color_tuple_to_int(tuple):
    r, g, b = tuple
    return (
        ((r & 0xFF) << (0x2 * 0x8))
        | ((b & 0xFF) << (0x1 * 0x8))
        | ((g & 0xFF) << (0x0 * 0x8))
    )


def clip_text(text: str, length: int) -> str:
    return f"{text[:length]}..." if len(text) > length else text


def get_url_extension(url, default="jpg"):
    match = re.search(url, r"\.(.+)\s*$")
    return match[1] if match else default


@contextmanager
def temporary_files(*files):
    try:
        yield files
    finally:
        logging.debug(f"Removing the following temporary files: '{files}'")
        for f in files:
            if os.path.exists(f) and os.path.isfile(f):
                os.remove(f)
                logging.debug(f"Removed '{f}'")
        logging.debug(f"Removed the following temporary files: '{files}'")


def split_by_length(item, maxlen: int):
    return [item[i : i + maxlen] for i in range(0, len(item), maxlen)]
